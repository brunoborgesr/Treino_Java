package exerciciosS1.tipos_primitivos;

public class variaveisPrimitivos {
    public static void main(String[] args){
        byte n1 = 127;
        short n2 = 32767;
        int n3 = 2147483647;
        long n4 = 9223372036854775807L;
        float n5 = 7.999999999999999f;
        double n6 = 8.4444444444444444;
        char character = 'M';
        boolean bool = true;
        System.out.println("valor byte: " + n1);
        System.out.println("valor short: " + n2);
        System.out.println("valor int: " + n3);
        System.out.println("valor long: " + n4);
        System.out.println("valor float: " + n5);
        System.out.println("valor double: " + n6);
        System.out.println("valor char: " + character);
        System.out.println("valor booleano: " + bool);
    }
}
